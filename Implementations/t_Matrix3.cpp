BOOST_AUTO_TEST_CASE(Matrix3OperatorMultiply) {
    TM::Matrix3 A
    (
        TM::Vector3(3.0f, 5.0f, 7.0f),
        TM::Vector3(6.0f, 3.0f, 8.0f),
        TM::Vector3(9.0f, 3.0f, 5.0f)
    );

    TM::Matrix3 B
    (
        TM::Vector3(2.0f, 5.0f, 1.0f),
        TM::Vector3(6.0f, 2.0f, 2.0f),
        TM::Vector3(2.0f, 3.0f, 2.0f)
    );

    TM::Matrix3 C = A * B;
    const glm::mat3 data = C.GetRawData();

    BOOST_CHECK_EQUAL(data[0].x, 45.0f);
    BOOST_CHECK_EQUAL(data[0].y, 28.0f);
    BOOST_CHECK_EQUAL(data[0].z, 59.0f);
    
    BOOST_CHECK_EQUAL(data[1].x, 48.0f);
    BOOST_CHECK_EQUAL(data[1].y, 42.0f);
    BOOST_CHECK_EQUAL(data[1].z, 68.0f);
    
    BOOST_CHECK_EQUAL(data[2].x, 42.0f);
    BOOST_CHECK_EQUAL(data[2].y, 25.0f);
    BOOST_CHECK_EQUAL(data[2].z, 48.0f);
}


