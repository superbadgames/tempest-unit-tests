    BOOST_CHECK_EQUAL(RoundFloat(data1[1].y), 0.92718f);
    BOOST_CHECK_EQUAL(RoundFloat(data1[1].z), 0.37461f);
    
    BOOST_CHECK_EQUAL(data1[2].x, 0.0f);
    BOOST_CHECK_EQUAL(RoundFloat(data1[2].y), -0.37461f);
    BOOST_CHECK_EQUAL(RoundFloat(data1[2].z), 0.92718f);

    mat.RotateY(30.0f);
