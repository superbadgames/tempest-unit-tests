BOOST_AUTO_TEST_CASE(Matrix3Inverse)
{
    TM::Matrix3 mat1 
    { 
        3.0f, 18.0f, 1.0f,
        8.0f, 0.0f, 9.0f,
        1.0f, 2.0f, 0.0f
    };

    mat1.SetInverse();
    const glm::mat3 data1 = mat1.GetRawData();

    BOOST_CHECK_EQUAL(RoundFloat(data1[0].x), -0.14516f);
    BOOST_CHECK_EQUAL(RoundFloat(data1[0].y), 0.01613f);
    BOOST_CHECK_EQUAL(RoundFloat(data1[0].z), 1.30645f);
    
    BOOST_CHECK_EQUAL(RoundFloat(data1[1].x), 0.07258f);
    BOOST_CHECK_EQUAL(RoundFloat(data1[1].y), -0.00806f);
    BOOST_CHECK_EQUAL(RoundFloat(data1[1].z), -0.15323f);
    
    BOOST_CHECK_EQUAL(RoundFloat(data1[2].x), 0.12903f);
    BOOST_CHECK_EQUAL(RoundFloat(data1[2].y), 0.09677f);
    BOOST_CHECK_EQUAL(RoundFloat(data1[2].z), -1.16129f);

    TM::Matrix3 mat3
    {
        8.0f, 6.0f, 7.0f,
        6.0f, 7.0f, 8.0f,
        7.0f, 8.0f, 6.0f
    };

    TM::Matrix3 mat4 = mat3.GetInverse();
    const glm::mat3 data2 = mat4.GetRawData();

    BOOST_CHECK_EQUAL(RoundFloat(data2[0].x), 0.34921f);
    BOOST_CHECK_EQUAL(RoundFloat(data2[0].y), -0.31746f);
    BOOST_CHECK_EQUAL(RoundFloat(data2[0].z), 0.01587f);
    
    BOOST_CHECK_EQUAL(RoundFloat(data2[1].x), -0.31746f);
    BOOST_CHECK_EQUAL(RoundFloat(data2[1].y), 0.01587f);
    BOOST_CHECK_EQUAL(RoundFloat(data2[1].z), 0.34921f);
    
    BOOST_CHECK_EQUAL(RoundFloat(data2[2].x), 0.01587f);
    BOOST_CHECK_EQUAL(RoundFloat(data2[2].y), 0.34921f);
    BOOST_CHECK_EQUAL(RoundFloat(data2[2].z), -0.317460001f);
}